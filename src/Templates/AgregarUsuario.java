    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        try {
             EmpleadoDAO modusu = new EmpleadoDAO();
        int Rut = Integer.parseInt(txtRut.getText());
        String Nombre = txtNombre.getText();
        String Apellido = txtApellido.getText();
        String Usuario = txtUsuario.getText();
        String Clave = txtClave.getText();
        String Tipo = (String)CbxTipo.getSelectedItem();
        if (Tipo.equals("Administrador")) {
            int respuesta = 1;
            int resp = modusu.AgregarEmpleado(Rut, Nombre, Apellido, Usuario, Clave, respuesta);
            System.out.println(resp);
        }else if (Tipo.equals("Recepcionista")) {
            int respuesta = 3;
            int resp = modusu.AgregarEmpleado(  Rut, Nombre, Apellido, Usuario, Clave, respuesta);
        }else if (Tipo.equals("Mecanico")) {
            int respuesta = 2;
            int resp = modusu.AgregarEmpleado(  Rut, Nombre, Apellido, Usuario, Clave, respuesta);
        }
        JOptionPane.showMessageDialog( null,"Usuario agregado correctamente");
        this.setVisible(false);
        new GestionUsuariosAdmin().setVisible(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
       
    }//GEN-LAST:event_btnGuardarMouseClicked

