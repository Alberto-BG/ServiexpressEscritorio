    private void btnTomarHoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTomarHoraMouseClicked
        
        if (this.Seleccionado != null) {
            //Hacer update a Mecanico = this.rut
            String result = new ClienteDAO().TomarHora(this.rut, this.Seleccionado);
            JOptionPane.showMessageDialog(null, result);
            this.setVisible(false);
            MainMecanico m = new MainMecanico();
            m.rut = this.rut;
            m.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un Cliente");
        }
    }//GEN-LAST:event_btnTomarHoraMouseClicked

