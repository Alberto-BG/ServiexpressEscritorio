    private void LlenarTodo() {
        try {
            ArrayList<Model.ClienteParametros> listado = new ClienteDAO().ListadoMecanico();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Rut");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido");
            modelo.addColumn("Patente");
            modelo.addColumn("Mecanico");
            modelo.addColumn("Estado");
            String[] fila = new String[6];
            JTableClientes.setModel(modelo);
            for(Model.ClienteParametros item:listado){
                fila[0]=item.getRutCliente();
                fila[1]=item.getNombreCliente();
                fila[2]=item.getApellidoCliente();
                fila[3]=item.getPatenteCliente();
                fila[4]=item.getMecanico();
                fila[5]=item.getEstado();
                modelo.addRow(fila);
            }
            JTableClientes.setModel(modelo);
        } catch (Exception e) {
        }
    }
