    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
       EmpleadoDAO modusu = new EmpleadoDAO();
        int Rut = Integer.parseInt(txtRutNuevo.getText());
        String Nombre = txtNombreNuevo.getText();
        String Apellido = txtApellidoNuevo.getText();
        String Usuario = txtUsuarioNuevo.getText();
        String Clave = TxtClave.getText();
        String Tipo = (String)cmbTipoNuevo.getSelectedItem();
        if (Tipo.equals("Administrador")) {
            int respuesta = 1;
            int resp = modusu .Modificar(Rut, Nombre, Apellido, Usuario, Clave, respuesta);
        }else if (Tipo.equals("Recepcionista")) {
            int respuesta = 3;
            int resp = modusu .Modificar(Rut, Nombre, Apellido, Usuario, Clave, respuesta);
        }else if (Tipo.equals("Mecanico")) {
            int respuesta = 2;
            int resp = modusu .Modificar(Rut, Nombre, Apellido, Usuario, Clave, respuesta);
        }
        this.setVisible(false);
 
    }//GEN-LAST:event_btnModificarMouseClicked

