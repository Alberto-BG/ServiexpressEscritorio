    private void LlenarTodo() {
        try {
            ArrayList<Model.Cliente> listado = new ClienteDAO().Listado();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Rut");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido");
            modelo.addColumn("Patente");
            String[] fila = new String[5];
            jTableUsuarios.setModel(modelo);
            for(Model.Cliente item:listado){
                fila[0]=item.getRutCliente();
                fila[1]=item.getNombreCliente();
                fila[2]=item.getApellidoCliente();
                fila[3]=item.getPatenteCliente();
                modelo.addRow(fila);
            }
            jTableUsuarios.setModel(modelo);
        } catch (Exception e) {
        }
    }
