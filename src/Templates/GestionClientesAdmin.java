    private void jTableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuariosMouseClicked
            int seleccionar = jTableUsuarios.rowAtPoint(evt.getPoint());
            String rutcliente = String.valueOf(jTableUsuarios.getValueAt(seleccionar, 0));
            String nombreCliente = String.valueOf(jTableUsuarios.getValueAt(seleccionar, 1));
            String apellidoCliente = String.valueOf(jTableUsuarios.getValueAt(seleccionar, 2));
            String patenteCliente = String.valueOf(jTableUsuarios.getValueAt(seleccionar, 3));
            Cliente cl = new Cliente(rutcliente,nombreCliente, apellidoCliente, patenteCliente);
            ModificarCliente mod = new ModificarCliente();
            ClienteDAO dc = new ClienteDAO();
            ClienteFecha cf = dc.ObtenerFecha(rutcliente);
            
            
            mod.txtRutActual.setText(rutcliente);
            mod.TxtRut.setText(rutcliente);
            mod.txtNombreActual.setText(nombreCliente);
            mod.TxtNombre.setText(nombreCliente);
            mod.txtApellidoActual.setText(apellidoCliente);
            mod.TxtApellido.setText(apellidoCliente);
            mod.txtPatenteActual.setText(patenteCliente);
            mod.TxtPatente.setText(patenteCliente);
            mod.txtFechaActual.setText(cf.fecha.substring(0,10));
            mod.rut = this.rut;
            mod.setVisible(true);
            
    }//GEN-LAST:event_jTableUsuariosMouseClicked

