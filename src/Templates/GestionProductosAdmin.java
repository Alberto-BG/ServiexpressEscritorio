    private void tableListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableListaMouseClicked
        int seleccionar = tableLista.rowAtPoint(evt.getPoint());
        String Id = String.valueOf(tableLista.getValueAt(seleccionar, 0));
        String Nombre = String.valueOf(tableLista.getValueAt(seleccionar, 1));
        String Descripcion = String.valueOf(tableLista.getValueAt(seleccionar, 2));
        String Familia = String.valueOf(tableLista.getValueAt(seleccionar, 3));
        String Fecha = String.valueOf(tableLista.getValueAt(seleccionar, 4));
        String precio = String.valueOf(tableLista.getValueAt(seleccionar, 5));
        Orden or = new Orden();
        or.txtProducto.setText(Id);
        or.TxtNombre.setText(Nombre);
        or.TxtDetalle.setText(Descripcion);
        or.TxtFamilia.setText(Familia);
        or.TxtFecha.setText(Fecha);
        or.precio = Integer.valueOf(precio);
        or.Txtprecio.setText(precio);
        or.rut = this.rut;
        or.proveedor = this.Proveedor;
        or.setVisible(true);
    }//GEN-LAST:event_tableListaMouseClicked

