    private void LlenarTodo(String nombre) {
         try {
            ArrayList<Model.ProductosProveedor> listado= new ProveedorDAO().GetProductosProveedor(nombre);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Id");
            modelo.addColumn("Nombre");
            modelo.addColumn("Descripcion");
            modelo.addColumn("Familia");
            modelo.addColumn("Fecha");
            modelo.addColumn("Precio");
            modelo.addColumn("Stock");
            String[] fila = new String[7];
            tableLista.setModel(modelo);
            for(Model.ProductosProveedor item:listado){
                fila[0]=String.valueOf(item.getID());
                fila[1]=item.getNombre();
                fila[2]=item.getDescripcion();
                fila[3]=item.getFamilia();
                fila[4]=item.getFecha_Ve().substring(0, 10);
                fila[5]=String.valueOf(item.getPrecio());
                fila[6]=String.valueOf(item.getStock());
                modelo.addRow(fila);
            }
            tableLista.setModel(modelo);
        } catch (Exception e) {
        }
    }
    private void LlenarCombo(){
       ArrayList<Model.Proveedor> listado = new ProveedorDAO().Listado();
       for(Model.Proveedor item:listado){
           CbxProveedores.addItem(item.getNombre());
       }
    }
