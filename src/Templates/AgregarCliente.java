    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
            try{
                String fechaSQL = "";
                Date fecha = JDate.getDate();
        
                if(fecha == null){
                    System.out.println(fecha);
                }else{
                    SimpleDateFormat DATE_FORMAT = new SimpleDateFormat("yyyy-MM-dd");
                    fechaSQL = DATE_FORMAT.format(fecha);
                }
                String Rut = TxtRut.getText();
                String Nombre = TxtNombre.getText();
                String Apellido = TxtApellido.getText();
                String Patente = TxtNombre.getText();
                int Telefono = Integer.parseInt(TxtTelefono.getText());
                String Detalle = txtProblema.getText();
                ClienteDAO modcli = new ClienteDAO();
                int resp = modcli.AgregarCliente(Rut,Nombre,Apellido,Patente,Telefono,fechaSQL,Detalle);
                System.out.println(resp);
                JOptionPane.showMessageDialog( null,"Hora agendada correctamente");
                }catch (Exception e){
                    JOptionPane.showMessageDialog( null,"Error, Verifica los datos");
                }
            
        
            //int resp = modcli.Agregar( Rut, Nombre, Apellido, Patente, Telefono,fechaSQL, Detalle);
            
    }//GEN-LAST:event_btnGuardarMouseClicked

