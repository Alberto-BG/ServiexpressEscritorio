    private void LlenarTodo() {
        try {
            ArrayList<Model.Empleado> listado = new EmpleadoDAO().Listado();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Rut");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido");
            modelo.addColumn("Tipo Empleado");
            String[] fila = new String[4];
            JTableUsuarios.setModel(modelo);
            for(Model.Empleado item:listado){
                fila[0]=item.getRutEmpleado();
                fila[1]=item.getNombreEmpleado();
                fila[2]=item.getApellidoEmpleado();
                fila[3]=item.getTipoEmpleado();
                modelo.addRow(fila);
            }
            JTableUsuarios.setModel(modelo);
        } catch (Exception e) {
        }
    }


