    private void JTableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableUsuariosMouseClicked
        int seleccionar = JTableUsuarios.rowAtPoint(evt.getPoint());
            String rutEmpleado = String.valueOf(JTableUsuarios.getValueAt(seleccionar, 0));
            Empleado listado = new EmpleadoDAO().GetEmpleado(rutEmpleado);
            ModificarUsuario mod = new ModificarUsuario();
            mod.setVisible(true);
            mod.txtRutNuevo.setText(listado.getRutEmpleado());
            mod.txtRutActual.setText(listado.getRutEmpleado());
            mod.txtNombreActual.setText(listado.getNombreEmpleado());
            mod.txtNombreNuevo.setText(listado.getNombreEmpleado());
            mod.txtApellidoActual.setText(listado.getApellidoEmpleado());
            mod.txtApellidoNuevo.setText(listado.getApellidoEmpleado());
            mod.txtUsuarioActual.setText(listado.getUsuarioEmpleado());
            mod.txtUsuarioNuevo.setText(listado.getUsuarioEmpleado());
            mod.txtTipoActual.setText(listado.getTipoEmpleado());
            mod.cmbTipoNuevo.setSelectedItem(listado.getTipoEmpleado());
    }//GEN-LAST:event_JTableUsuariosMouseClicked

